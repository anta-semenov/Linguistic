import Foundation
import UIKit

extension UIColor {
    class func lgLessonItemColor() -> UIColor {
        return UIColor(red: 223.0/255.0, green: 223.0/255.0, blue: 223.0/255.0, alpha: 1.0)
    }
    
    class func lgLessonSelectItemColor() -> UIColor {
        return UIColor(red: 0.0, green: 122.0/255.0, blue: 1.0, alpha: 0.5)
    }
}
